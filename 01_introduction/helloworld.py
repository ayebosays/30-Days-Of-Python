# Day 1 - 30DaysOfPython Challenge

print(2 + 3)                    # addition(+)
print(3 - 1)                    # subtraction(-)
print(2 * 3)                    # multiplication(*)
print(3 / 2)                    # division(/)
print(3 ** 2)                   # exponential(**)
print(3 % 2)                    # modulus(%)
print(3 // 2)                   # floor division operator(//)

# Checking data types
print(type(10))                 # Int (negative, zero and positive number)
print(type(3.14))               # Float (decimal number)
print(type(1 + 3j))             # Complex number
print(type('Abel'))             # String (collection of one or more characters under single or double quote, triple if more than one sentence)
print(type([1, 2, 3]))          # List (an ordered collection allowing storage of different data types, similar to array in JavaScript)
print(type({'name':'Abel'}))    # Dictionary (an unordered collection of data in a key:value pair format)
print(type({9.8, 3.14, 2.7}))   # Set (an unorded collection of data types allowing only unique items)
print(type(9.8, 3.14, 2.7))     # Tuple (an ordered collection of different data types like list but cannot be modified once created, are immutable)